void main(){

  var marks=70;

  switch(marks){

    case 80:
      print('A+');
      break;
    case 70:
      print('A');
      break;
    case 60:
      print('A-');
      break;
    case 50:
      print('B');
      break;
    case 40:
      print('C');
      break;

    default:
      print('fail');
  }
}
